// ── zip & download ──────────────────────────────────────────────

export async function generateAndDownload(state: DesignState): Promise<void> {
  const zip = new JSZip();
  const folder = zip.folder("design-system")!;

  folder.file("tokens.css", generateTokensCSS(state));
  folder.file("tokens.ts", generateTokensTS(state));
  folder.file("Text.tsx", generateTextTSX(state));
  folder.file("Button.tsx", generateButtonTSX(state));
  folder.file("Card.tsx", generateCardTSX(state));
  folder.file("Section.tsx", generateSectionTSX(state));

  const blob = await zip.generateAsync({ type: "blob" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${(state.projectName || "design-system").toLowerCase().replace(/\s+/g, "-")}-components.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
