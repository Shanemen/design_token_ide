// ── Types ────────────────────────────────────────────────────────

interface TypeLevel {
  name: string;
  size: string;
  weight: string;
  lineHeight: string;
  font: string;
}

interface Colors {
  bg: string;
  surface: string;
  textPrimary: string;
  textSecondary: string;
  accent: string;
  warning: string;
  success: string;
}

interface SpacingSemantic {
  inlineY: number;
  inlineX: number;
  content: number;
  element: number;
  block: number;
  section: number;
  page: number;
}

interface DesignState {
  projectName: string;
  maxContentWidth: string;
  headingFont: string;
  bodyFont: string;
  typeLevels: TypeLevel[];
  colors: Colors;
  density: string;
  borderRadius: string;
  borderWidth: string;
  borderColor: string;
  borderComponents: Record<string, boolean>;
  radiusOverrides: Record<string, string>;
  motionLevel: string;
  easingStyle: string;
  defaultDuration: string;
  hoverEffects: boolean;
  hoverStyle: string;
  dontList: string;
  breakpoints: string;
  mobileFirst: boolean;
  states: string;
  visitedSections: Record<string, boolean>;
  components: Record<string, any>;
}

// ── Helpers ──────────────────────────────────────────────────────

function toKebab(name: string): string {
  return name.toLowerCase().replace(/\s+/g, "-");
}

function toCamel(name: string): string {
  return name
    .split(/\s+/)
    .map((w, i) => (i === 0 ? w.toLowerCase() : w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()))
    .join("");
}

function easingValue(style: string): string {
  if (style === "spring") return "cubic-bezier(0.34, 1.56, 0.64, 1)";
  if (style === "ease-out") return "cubic-bezier(0.22, 1, 0.36, 1)";
  if (style === "ease-in-out") return "cubic-bezier(0.45, 0, 0.55, 1)";
  return "linear";
}

function spacingOf(s: DesignState): SpacingSemantic {
  return (DENSITY_PRESETS[s.density || "balanced"] || DENSITY_PRESETS.balanced).semantic;
}


function visited(s: DesignState, key: string): boolean {
  return !!(s.visitedSections || {})[key];
}

function comp(s: DesignState, id: string): any {
  return (s.components || {})[id] || {};
}

function radiusNum(s: DesignState, compId?: string): number {
  if (compId && s.radiusOverrides?.[compId]) return parseInt(s.radiusOverrides[compId]);
  return parseInt(s.borderRadius) || 8;
}

function radiusVar(s: DesignState, compId?: string): string {
  if (compId && s.radiusOverrides?.[compId]) return `"var(--radius-${toKebab(compId)})"`;
  return `"var(--radius)"`;
}

function hasBorder(s: DesignState, compId: string): boolean {
  return !!(s.borderComponents || {})[compId];
}

const GENERIC_FONTS = ["serif", "sans-serif", "monospace", "cursive", "fantasy", "system-ui"];

function fontCSSValue(font: string): string {
  const f = font || "sans-serif";
  if (GENERIC_FONTS.includes(f.toLowerCase())) return f;
  return `'${f}', sans-serif`;
}

