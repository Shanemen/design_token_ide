// ── tokens.css ──────────────────────────────────────────────────

function generateTokensCSS(s: DesignState): string {
  const sp = spacingOf(s);
  const easing = easingValue(s.easingStyle);
  const hFont = s.headingFont || "sans-serif";
  const bFont = s.bodyFont || "sans-serif";

  // Google fonts import
  const googleFonts = [hFont, bFont]
    .filter(f => f && !["sans-serif"].includes(f))
    .map(f => f.replace(/ /g, "+"));
  const uniqueFonts = [...new Set(googleFonts)];

  let css = `/* Design Tokens — ${s.projectName || "Untitled"} */\n`;
  css += `/* Generated by Design Token IDE */\n\n`;

  if (uniqueFonts.length > 0) {
    css += `@import url('https://fonts.googleapis.com/css2?${uniqueFonts.map(f => `family=${f}:wght@300;400;500;600;700`).join("&")}&display=swap');\n\n`;
  }

  css += `:root {\n`;

  // Fonts
  css += `  /* ── Fonts ── */\n`;
  css += `  --font-heading: ${fontCSSValue(hFont)};\n`;
  css += `  --font-body: ${fontCSSValue(bFont)};\n\n`;

  // Typography levels
  css += `  /* ── Typography ── */\n`;
  s.typeLevels.forEach(t => {
    const key = toKebab(t.name);
    css += `  --type-${key}-size: ${t.size}px;\n`;
    css += `  --type-${key}-weight: ${t.weight};\n`;
    css += `  --type-${key}-line-height: ${t.lineHeight};\n`;
  });
  css += `\n`;

  // Colors
  css += `  /* ── Colors ── */\n`;
  css += `  --color-bg: ${s.colors.bg};\n`;
  css += `  --color-surface: ${s.colors.surface};\n`;
  css += `  --color-text-primary: ${s.colors.textPrimary};\n`;
  css += `  --color-text-secondary: ${s.colors.textSecondary};\n`;
  css += `  --color-accent: ${s.colors.accent};\n`;
  css += `  --color-warning: ${s.colors.warning};\n`;
  css += `  --color-success: ${s.colors.success};\n\n`;

  // Spacing — semantic tokens derived from density
  css += `  /* ── Spacing (density: ${s.density || "balanced"}) ── */\n`;
  css += `  --space-inline-y: ${sp.inlineY}px;\n`;
  css += `  --space-inline-x: ${sp.inlineX}px;\n`;
  css += `  --space-content: ${sp.content}px;\n`;
  css += `  --space-element: ${sp.element}px;\n`;
  css += `  --space-block: ${sp.block}px;\n`;
  css += `  --space-section: ${sp.section}px;\n`;
  css += `  --space-page: ${sp.page}px;\n`;
  css += `  --max-content-width: ${s.maxContentWidth === "full" ? "100%" : s.maxContentWidth + "px"};\n\n`;

  // Border & Radius
  css += `  /* ── Border & Radius ── */\n`;
  css += `  --radius: ${s.borderRadius}px;\n`;
  css += `  --border-width: ${s.borderWidth}px;\n`;
  css += `  --border-color: ${s.borderColor};\n`;
  // Per-component radius overrides
  const ro = s.radiusOverrides || {};
  for (const [compId, val] of Object.entries(ro)) {
    if (val) css += `  --radius-${toKebab(compId)}: ${val}px;\n`;
  }
  css += `\n`;

  // Motion
  css += `  /* ── Motion ── */\n`;
  css += `  --duration: ${s.defaultDuration}ms;\n`;
  css += `  --easing: ${easing};\n`;
  css += `}\n`;

  // Breakpoints
  const bps = s.breakpoints.split("/").map(v => v.trim()).filter(Boolean);
  if (bps.length > 0) {
    css += `\n/* ── Breakpoints (${s.mobileFirst ? "mobile-first" : "desktop-first"}) ── */\n`;
    bps.forEach((bp, i) => {
      css += `/* @custom-media --bp-${i + 1}: (${s.mobileFirst ? "min" : "max"}-width: ${bp}px) */\n`;
    });
  }

  return css;
}

